var_name="books"
#string methods
print(len(var_name))  #the len method returns the length of an object.
print(var_name.index("k")) #the index method helps you to find the index position of an element.
print(var_name.capitalize()) #the capitalize method returns a string with the very first letter capitalised.
print(var_name.casefold()) #the casefold converts all characters the string into lowercase letters.
print(var_name.isalpha()) #the isalpha tells you if a string is written in alphabets.
print(var_name.isdigit()) #the isdigit checks if all the characters in a string are digit values or not.
print(var_name.islower()) #the islower tells if your string is in lowercase or not.
